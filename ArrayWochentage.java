
public class ArrayWochentage {
	public static void main(String[] args) {
		String[] wochentage = {"Montag", "Dienstag", "Mittwoch", "Donnerstag","Freitag", "Samstag", "Sonntag"};
		
		
			int zaehler = 0;
			while (zaehler < wochentage.length) {
				
				System.out.println(wochentage[zaehler] );
				zaehler = zaehler + 1 ;
			
			}
			
	}

}
